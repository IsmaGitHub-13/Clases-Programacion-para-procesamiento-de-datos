
import figuras
lado_c = 5
resultado_cuadrado = figuras.areacuadrado(lado_c)
print(f"El área de un cuadrado de lado {lado_c} es: {resultado_cuadrado}")

base_t = 4
altura_t = 6
resultado_triangulo = figuras.areatriangulo(base_t, altura_t)
print(f"El área de un triángulo de base {base_t} y altura {altura_t} es: {resultado_triangulo}")

base_r = 8
altura_r = 3
resultado_rectangulo = figuras.arearectangulo(base_r, altura_r)
